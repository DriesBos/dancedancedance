test('column slider memoizes active images and renders indicators once', () => {
  const source = readSource('./storyblok/ColumnSlider.tsx');
  const activeImagesIndex = source.indexOf('const activeImages = useMemo(');
  const mapIndex = source.indexOf('activeImages.map');
  const indicatorsIndex = source.indexOf('<SliderIndicators');

  assert.notEqual(activeImagesIndex, -1, 'activeImages should be memoized');
  assert.ok(
    activeImagesIndex < mapIndex,
    'activeImages should be derived before rendering slides',
  );
  assert.equal(
    (source.match(/<SliderIndicators/g) || []).length,
    1,
    'SliderIndicators should be rendered once per slider',
  );
  assert.ok(
    indicatorsIndex > source.lastIndexOf('})}', source.indexOf('{blok.caption')),
    'SliderIndicators should be outside the activeImages.map render loop',
  );
});

